// Shared helpers for Sitp GPT expansion pages.
// Purely client-side mocks — mirrors the no-backend approach used in sitpgpt.html.

// Built-in viral propagation: any content generated on a Free plan carries a
// "Powered by Sitp GPT" watermark that links back to the homepage. Pro/Business
// plans remove it (see removeWatermarkForPlan()). Since this static demo has no
// real accounts, the current plan is tracked in localStorage so the badge can be
// toggled off after a (simulated) upgrade.
const SPG_HOME = (function () {
  // Works whether the page lives at the site root or inside /tools/ or /seo/.
  const depth = window.location.pathname.split('/').filter(Boolean).length;
  const inSubfolder = window.location.pathname.includes('/tools/') || window.location.pathname.includes('/seo/');
  return inSubfolder ? '../sitpgpt.html' : 'sitpgpt.html';
})();

function spgCurrentPlan() {
  return localStorage.getItem('spg_plan') || 'free';
}

function spgSetPlan(plan) {
  localStorage.setItem('spg_plan', plan);
}

function spgWatermarkHTML() {
  if (spgCurrentPlan() !== 'free') return '';
  return `<div class="spg-watermark">⚡ Powered by <a href="${SPG_HOME}" target="_blank" rel="noopener">Sitp GPT</a></div>`;
}

// Appends the watermark to any element generated by a free-plan tool/report/chat output.
function spgAttachWatermark(targetEl) {
  if (!targetEl) return;
  const existing = targetEl.querySelector('.spg-watermark');
  if (existing) existing.remove();
  const html = spgWatermarkHTML();
  if (html) targetEl.insertAdjacentHTML('beforeend', html);
}

function spgMockPay(planName, priceLabel) {
  alert(`✅ ${planName} 订阅已成功！（浏览器内模拟，零服务器）\n价格：${priceLabel}\n你现在可以访问 ${planName} 的全部功能了！感谢购买 Sitp GPT。`);
  const key = planName.toLowerCase().includes('free') ? 'free' : (planName.toLowerCase().includes('setup') ? spgCurrentPlan() : 'pro');
  spgSetPlan(key);
}

function spgMockSetupPurchase() {
  alert('✅ 一次性AI客服安装服务（$599）购买成功！（浏览器内模拟）\n我们的团队将在1个工作日内与你联系，开始训练AI、安装网站机器人、设置FAQ并优化客服流程。\n该服务同时解锁所有付费套餐内容。');
  spgSetPlan('pro');
}
